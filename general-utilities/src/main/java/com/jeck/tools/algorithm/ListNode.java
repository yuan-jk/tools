package com.jeck.tools.algorithm;

public class ListNode {
    int val;
    ListNode next;

    public static void main(String[] args) {



    }


    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
